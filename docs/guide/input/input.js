export const inputGuide = `### PROPS
+ modelValue
  + String, Number
  + v-model 가능, 반응형 입력값
+ type
  + String
  + html 기본 type (month, textarea 등)


+ label
  + String
  + 박스 좌측 상단에 표시되는 라벨
+ placeholder
  + String
  + 미입력 시 표시되는 기본 텍스트
+ hintMessage
  + String
  + 박스 좌측 하단에 표시되는 에러 색상 메시지


+ clearable
  + Boolean
  + 입력값을 삭제하는 아이콘 표시 여부


+ iconName
  + String, Null
  + 구글 material-icons 기반 아이콘 이름
+ leftIcon
  + Boolean
    + default: false
  + 아이콘 좌측 표시 여부 (기본: 우측 표시)
+ iconClickable
  + Boolean
  + 아이콘 클릭 가능 여부
`
